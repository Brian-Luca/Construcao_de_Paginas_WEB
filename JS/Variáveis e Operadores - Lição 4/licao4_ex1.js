var mult= x*y;   // Multiplicação
var div= x/y;   // Divisão
var sub= x-y;  // Subtracção
var mod= x%y; // Resto da Divisão Inteira

//Resultados

document.write("x=",x,", y=",y,", z=",z,"<p/>");
